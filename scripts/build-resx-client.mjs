import fs from "node:fs";
import path from "node:path";

const projectRoot = path.resolve(import.meta.dirname, "..");
const sourceFile = path.join(projectRoot, "src", "ResXClient.js");
const outputFile = path.join(projectRoot, "client", "ResXClient.js");

const source = fs.readFileSync(sourceFile, "utf8");
const normalizedSource = source
  .replace(/^\/\/ Generated by ReScript, PLEASE EDIT WITH CARE\s*/u, "")
  .replace(/^'use strict';\s*/u, "")
  .replace(/\n\/\*\s+Not a pure module\s+\*\/\s*$/u, "\n")
  .trim();

const output = `// Built browser entry for ResX.Client.\n// Source of truth: src/ResXClient.res\n// Regenerate with: npm run build:resx-client\n\n${normalizedSource}\n`;

fs.mkdirSync(path.dirname(outputFile), { recursive: true });
fs.writeFileSync(outputFile, output);
